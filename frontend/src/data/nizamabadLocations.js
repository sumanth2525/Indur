/** Flat list of all mandals with division metadata */
export const ALL_MANDALS = REVENUE_DIVISIONS.flatMap((div) =>
  div.mandals.map((m) => ({
    ...m,
    division: div.name,
    divisionId: div.id,
  })),
)

export const DEFAULT_LOCATION = {
  scope: 'district',
  mandal: null,
  village: null,
  division: null,
  label: DISTRICT,
}

export function normalizeLocation(raw) {
  if (!raw) return { ...DEFAULT_LOCATION }
  if (typeof raw === 'object' && raw.label) return raw
  if (typeof raw === 'string') {
    try {
      const parsed = JSON.parse(raw)
      if (parsed?.label) return parsed
    } catch {
      /* legacy string */
    }
    if (raw === DISTRICT) return { ...DEFAULT_LOCATION }
    const mandal = ALL_MANDALS.find(
      (m) => m.name.toLowerCase() === raw.toLowerCase() || raw.toLowerCase().includes(m.name.toLowerCase()),
    )
    if (mandal) {
      return { scope: 'mandal', mandal: mandal.name, village: null, division: mandal.division, label: mandal.name }
    }
    return { scope: 'mandal', mandal: raw, village: null, division: null, label: raw }
  }
  return { ...DEFAULT_LOCATION }
}

export function locationToStorage(loc) {
  return JSON.stringify(normalizeLocation(loc))
}

export function divisionLocation(divisionIdOrName) {
  const div = REVENUE_DIVISIONS.find(
    (d) => d.id === divisionIdOrName || d.name.toLowerCase() === String(divisionIdOrName).toLowerCase(),
  )
  if (!div) return { ...DEFAULT_LOCATION }
  return {
    scope: 'division',
    division: div.name,
    divisionId: div.id,
    mandal: null,
    village: null,
    label: div.name,
  }
}

export function getDivisionNames() {
  return REVENUE_DIVISIONS.map((d) => d.name)
}

export function mandalLocation(mandalName) {
  const m = ALL_MANDALS.find((x) => x.name === mandalName)
  if (!m) return { scope: 'mandal', mandal: mandalName, village: null, division: null, label: mandalName }
  return { scope: 'mandal', mandal: m.name, village: null, division: m.division, label: m.name }
}

export function villageLocation(mandalName, villageName) {
  const m = ALL_MANDALS.find((x) => x.name === mandalName)
  return {
    scope: 'village',
    mandal: mandalName,
    village: villageName,
    division: m?.division ?? null,
    label: `${villageName}, ${mandalName}`,
  }
}

export function searchLocations(query) {
  const q = query.trim().toLowerCase()
  if (!q) return []

  const results = []
  for (const div of REVENUE_DIVISIONS) {
    for (const mandal of div.mandals) {
      if (mandal.name.toLowerCase().includes(q)) {
        results.push({ type: 'mandal', ...mandalLocation(mandal.name), division: div.name })
      }
      for (const village of mandal.villages) {
        if (village.toLowerCase().includes(q)) {
          results.push({ type: 'village', ...villageLocation(mandal.name, village), division: div.name })
        }
      }
    }
  }
  return results.slice(0, 40)
}

function haversineKm(lat1, lng1, lat2, lng2) {
  const R = 6371
  const dLat = ((lat2 - lat1) * Math.PI) / 180
  const dLng = ((lng2 - lng1) * Math.PI) / 180
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) * Math.cos((lat2 * Math.PI) / 180) * Math.sin(dLng / 2) ** 2
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a))
}

export function nearestMandal(lat, lng) {
  let best = ALL_MANDALS[0]
  let bestDist = Infinity
  for (const m of ALL_MANDALS) {
    const d = haversineKm(lat, lng, m.lat, m.lng)
    if (d < bestDist) {
      bestDist = d
      best = m
    }
  }
  return mandalLocation(best.name)
}

export function matchLocationByName(city, region) {
  const text = `${city || ''} ${region || ''}`.toLowerCase()
  if (!text.includes('nizamabad') && !text.includes('telangana')) return null

  for (const m of ALL_MANDALS) {
    if (text.includes(m.name.toLowerCase())) return mandalLocation(m.name)
  }
  for (const div of REVENUE_DIVISIONS) {
    if (text.includes(div.name.toLowerCase())) {
      return mandalLocation(div.mandals[0].name)
    }
  }
  return { ...DEFAULT_LOCATION }
}

/** Property filter: does listing match selected location? */
export function propertyMatchesLocation(property, location) {
  const loc = normalizeLocation(location)
  if (loc.scope === 'district') return true

  const area = (property.location?.area || '').toLowerCase()
  const title = (property.title || '').toLowerCase()
  const propMandal = (property.location?.mandal || '').toLowerCase()
  const propVillage = (property.location?.village || '').toLowerCase()
  const mandal = (loc.mandal || '').toLowerCase()
  const village = (loc.village || '').toLowerCase()

  if (loc.scope === 'division') {
    const div = REVENUE_DIVISIONS.find(
      (d) => d.name === loc.division || d.id === loc.divisionId,
    )
    if (!div) return true
    const divisionName = div.name.toLowerCase()
    if (area.includes(divisionName) || title.includes(divisionName)) return true
    if (propMandal) {
      const inDivision = div.mandals.some((m) => m.name.toLowerCase() === propMandal)
      if (inDivision) return true
    }
    return div.mandals.some((m) => {
      const mName = m.name.toLowerCase()
      if (area.includes(mName) || title.includes(mName)) return true
      return m.villages.some(
        (v) => area.includes(v.toLowerCase()) || title.includes(v.toLowerCase()),
      )
    })
  }

  if (loc.scope === 'village' && village) {
    if (propVillage === village) return true
    return area.includes(village) || title.includes(village) || area.includes(mandal)
  }

  if (mandal) {
    if (propMandal === mandal) return true
    const m = ALL_MANDALS.find((x) => x.name.toLowerCase() === mandal)
    if (area.includes(mandal) || title.includes(mandal)) return true
    if (m?.villages.some((v) => area.includes(v.toLowerCase()) || title.includes(v.toLowerCase()))) return true
  }

  return false
}

